function resizeImagelg(image) {
  if (image.naturalHeight > image.naturalWidth) {
    image.style.width = "23vw";
    image.style.height = "17vw";
  }
}


function resizeImage(image) {
  if (image.naturalHeight > image.naturalWidth) {
    image.style.width = "90vw";
    image.style.height = "75vw";
  }
}

